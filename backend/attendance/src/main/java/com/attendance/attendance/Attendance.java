package com.attendance.attendance;

import java.util.Date;

public class Attendance {
  private Date date;
  private String name;

  public Date getDate() {
    return this.date;
  }

  public void setDate(Date date) {
    this.date = date;
  }

  public String getName() {
    return this.name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
